import unittest

class TestSlidingWindowAverage(unittest.TestCase):
    def setUp(self):
        self.swa = SlidingWindowAverage(window_size_seconds=1.0)

    def test_initial_state(self):
        self.assertEqual(len(self.swa.queue), 0)
        self.assertEqual(self.swa.total_sum, 0.0)

    def test_add_single_value(self):
        self.swa.add(10.0)
        self.assertEqual(len(self.swa.queue), 1)
        self.assertEqual(self.swa.total_sum, 10.0)
        self.assertAlmostEqual(self.swa.get_average(), 10.0, places=5)

    def test_add_multiple_values(self):
        self.swa.add(10.0)
        self.swa.add(20.0)
        self.assertEqual(len(self.swa.queue), 2)
        self.assertEqual(self.swa.total_sum, 30.0)
        self.assertAlmostEqual(self.swa.get_average(), 15.0, places=5)

